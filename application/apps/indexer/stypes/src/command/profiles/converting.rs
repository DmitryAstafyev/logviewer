use crate::*;

/// Converts a `envvars::Profile` into an `Profile`.
impl From<envvars::Profile> for Profile {
    fn from(pro: envvars::Profile) -> Self {
        Profile {
            name: pro.name,
            path: pro.path,
            envvars: pro.envvars,
            symlink: pro.symlink,
        }
    }
}
